async def start(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    if not await require_access(update, context):
        return
    user = update.effective_user
    upsert_user(user.id, user.username or "", user.first_name or "")
    await show_home(update, context)


async def button_router(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    query = update.callback_query
    await query.answer()

    if not await require_access(update, context):
        return

    user = update.effective_user
    upsert_user(user.id, user.username or "", user.first_name or "")

    data = query.data or ""

    if data == "V":
        await show_home(update, context, edit=True)
        return

    if data == "H:home":
        await show_home(update, context, edit=True)
        return

    if data == "H:lessons":
        await show_lessons(update, context)
        return

    if data == "H:progress":
        await show_progress(update, context)
        return

    if data == "H:bonuses":
        await show_bonuses(update, context)
        return

    if data.startswith("O:"):
        lesson_num = int(data.split(":")[1])
        await show_lesson_options(update, context, lesson_num)
        return

    if data.startswith("M:"):
        _, lesson_str, mode = data.split(":")
        lesson_num = int(lesson_str)
        if mode == "t":
            await send_lesson_text(update, context, lesson_num)
        else:
            await send_lesson_video_placeholder(update, context, lesson_num)
        return

    if data.startswith("C:"):
        lesson_num = int(data.split(":")[1])
        mark_completed(user.id, lesson_num)
        if lesson_num >= TOTAL_LESSONS:
            await finish_course(update, context)
        else:
            text = (
                f"✅ Урок {lesson_num} отмечен как изученный.\n\n"
                f"Следующий доступный урок: {lesson_num + 1}"
            )
            keyboard = InlineKeyboardMarkup(
                [
                    [
                        InlineKeyboardButton("➡️ Открыть следующий урок", callback_data=f"O:{lesson_num + 1}")
                    ],
                    [InlineKeyboardButton("📚 Ко всем урокам", callback_data="H:lessons")],
                ]
            )
            await safe_edit(query, text, keyboard)
        return


def build_application() -> Application:
    if not BOT_TOKEN or BOT_TOKEN == "PASTE_BOT_TOKEN_HERE":
        raise RuntimeError("Укажи BOT_TOKEN в переменных окружения.")

    application = ApplicationBuilder().token(BOT_TOKEN).build()
    application.add_handler(CommandHandler("start", start))
    application.add_handler(CallbackQueryHandler(button_router))
    return application


def main() -> None:
    init_db()
    app = build_application()

    if WEBHOOK_URL:
        logger.info("Запуск через webhook...")
        app.run_webhook(
            listen="0.0.0.0",
            port=PORT,
            url_path=WEBHOOK_PATH,
            webhook_url=f"{WEBHOOK_URL.rstrip('/')}/{WEBHOOK_PATH}",
            secret_token=WEBHOOK_SECRET or None,
        )
    else:
        logger.info("Запуск через polling...")
        app.run_polling(allowed_updates=Update.ALL_TYPES)


if __name__ == "__main__":
    main()
